
# capitalize() => membuat kalimat capital pada kata pertama
x = "kalimat yang akan digunakan"
print(x.capitalize())

# casefold() => membuat huruf kecil semua
x.casefold()
print(x)

# center() => membuat kalimat ke tengah
a = "Sapi"
print(a.center(20))

# count() => menghitung jumlah kata yang muncul dari sebuah kalimat yang ada
klm = "coba saja kalau saya menjadi mansia yang hebat, saya akan menguasai bumi dan seluruh isinya"
print(klm.count("saya"))

# endwith() => mengecek huruf terakhir dari kalimat apakah menggunakan huruf berikut
ikm = "inisaya."
print(ikm.endswith("."))

# find() => mencari kata pada posisi kata keberapa
x = "apa kabar kamu?"
print(x.find("bar"))